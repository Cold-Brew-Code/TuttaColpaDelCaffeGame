    private String getImagePath(GameStatus s){
        String victory_type;
        victory_type = switch (s) {
            case VINTA -> "partita_vinta";
            case BAGNO_USATO -> "bocciato";
            default -> "partita_persa";
        };
        return "/images/"+victory_type+".png";
    }
